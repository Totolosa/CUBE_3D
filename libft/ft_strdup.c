#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		i;

	if ((dest = malloc(sizeof(char) * ft_strlen(s) + 1)) == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
